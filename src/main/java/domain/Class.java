package domain;

public class Class {
    private Integer class_id;
    private Integer class_type;
    private Integer class_cost;

    public Integer getClass_id() {
        return class_id;
    }

    public void setClass_id(Integer class_id) {
        this.class_id = class_id;
    }

    public Integer getClass_type() {
        return class_type;
    }

    public void setClass_type(Integer class_type) {
        this.class_type = class_type;
    }

    public Integer getClass_cost() {
        return class_cost;
    }

    public void setClass_cost(Integer class_cost) {
        this.class_cost = class_cost;
    }
}
